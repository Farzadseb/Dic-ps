// مدیریت جستجو
function handleSearch() {
    const word = wordInput.value.trim().toLowerCase();
    
    if (!word) {
        showError('لطفاً یک کلمه انگلیسی وارد کنید');
        return;
    }
    
    if (!A1_DICTIONARY[word]) {
        showError(`کلمه "${word}" در دیکشنری سطح A1 یافت نشد. کلمات مانند: be, have, go, see`);
        return;
    }
    
    showLoading();
    
    setTimeout(() => {
        displayWordResult(word);
        if (!isMuted) {
            speakWord(word);
        }
    }, 300);
}

